#ifndef POINT_CLOUD_H
#define POINT_CLOUD_H

#include <QVector>

#include <array>

//=======================================================================================
namespace csf
{

    //===================================================================================
    struct Point
    {
        union
        {
            struct
            {
                double x;
                double y;
                double z;
            };

            std::array<double, 3> u { 0, 0, 0 };
        };

        Point()
            : x (0),
              y (0),
              z (0)
        {}
    };
    //===================================================================================

    //===================================================================================
    class PointCloud : public QVector<Point>
    {
    public:

        void set_bounding_box( Point& pmin, Point& pmax );

    };
    //===================================================================================

}
//=======================================================================================

#endif // POINT_CLOUD_H_
