#ifndef POINT_CLOUD_H
#define POINT_CLOUD_H

#include <vector>

//=======================================================================================
namespace csf
{

    //===================================================================================
    struct Point
    {
        union
        {
            struct
            {
                double x;
                double y;
                double z;
            };

            double u[3];
        };

        Point()
            : x (0),
              y (0),
              z (0)
        {}
    };
    //===================================================================================

    //===================================================================================
    class PointCloud : public std::vector<Point>
    {
    public:

        void set_bounding_box( Point& bbMin, Point& bbMax );

    };
    //===================================================================================

}
//=======================================================================================

#endif // POINT_CLOUD_H_
